    private void btnGuardarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarClienteActionPerformed
        // TODO add your handling code here:
        
    if (txtDuiCliente.getText().trim().isEmpty() || txtNombreCliente.getText().trim().isEmpty() ||txtTelefonoCliente.getText().trim().isEmpty() || txtDireccionCliente.getText().trim().isEmpty()) {
      JOptionPane.showMessageDialog(null, "Campos Vacios");
    } else {
      cl.setDui(Integer.parseInt(txtDuiCliente.getText()));
      cl.setNombre(txtNombreCliente.getText());
      cl.setTelefono(Integer.parseInt(txtTelefonoCliente.getText()));
      cl.setDireccion(txtDireccionCliente.getText());
      cli.RegistrarCliente(cl);
      JOptionPane.showMessageDialog(null, "Clientes Registrados");
    } 
    }//GEN-LAST:event_btnGuardarClienteActionPerformed

